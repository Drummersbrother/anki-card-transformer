import requests
from multiprocessing.pool import ThreadPool
import time


def get_media_files(images_to_get: list, num_concurrent_downloads: int, auth_token: str):
    card_media_basedir = "card_media"
    card_img_namer = lambda inx: f"card_img_{inx}"

    print(f"Going to download {len(images_to_get)} images...")

    auth_query_params = {
        "access_token": auth_token}

    card_img_url = lambda inx: f"https://cdn-001.smart-prep.com/contentmedia/{inx}"

    def get_media_file(media_inx):
        done = False
        while not done:
            with requests.get(card_img_url(media_inx), params=auth_query_params) as media_req:
                file_type = media_req.headers["Content-Type"].split("/")[-1]
                if file_type == "svg+xml":
                    file_type = "svg"
                elif file_type == "text/html":
                    time.sleep(0.1)
                    continue
                with open(card_media_basedir + "/" + card_img_namer(media_inx) + f".{file_type}", mode="wb") as media_file:
                    media_file.write(media_req.content)
                    print(media_inx, media_req.headers["Content-Type"])
                done = True

    for result in ThreadPool(num_concurrent_downloads).map(get_media_file, images_to_get):
        pass

